function solution1(n) {
  let answer = 0;
  const prime = [];
  for (let i = 2; i <= n; i++) {
    let divisor = 0;
    for (let j = 0; j < i; j++) {
      if (i % prime[j] === 0) {
        divisor = 1;
        break;
      }
      if (prime[j] > Math.sqrt(i)) {
        break;
      }
    }
    if (divisor === 0) {
      answer++;
      prime.push(i);
    }
  }
  return answer;
}

console.log(solution1(100));

